                  4 of the 5 other midweek games away from home that season.</p>
              <p>- Also in 2011, I could not find photos from either the Lamar game on Feburary 19 or the the Iowa game on March 5. However, both of those games
                were part of doubleheaders, and I find it very likely that, in both instances, the same uniform was worn as in the other game that day.</p>
              <p>- For the 1979 and 1981 College World Series games, though you can't explicitly tell in photos, I assumed that gray pants were worn for games
                in which State was the designated "away" team and white pants were worn for games in which they were the designated "home" team.</p>
